export const dividir = (numberA, numberB) => {
    return +numberA / +numberB
}
export const multiplicar = (numberA, numberB) => {
    return +numberA * +numberB
}
export const somar = (numberA, numberB) => {
    return +numberA + +numberB
}
export const subtrair = (numberA, numberB) => {
    return +numberA - +numberB
}
